
public class OperadoresAsignacion {

	public static void main(String[] args) {
		int a = 3, b = 2;
		int c =  a + 5 - b;
		
		System.out.println("El valor de c = "+c);
		
		a +=1; //a = a + 1
		System.out.println("El valor de a = "+a);
		
		a +=3; // a = a + 3
		System.out.println("El valor de a = "+a);
		
		a -=2;// a = a - 2
		System.out.println("El valor de a = "+a);
		
		// Practicar  *= /= %=
	}

}
